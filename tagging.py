def get_pos(word):
    # Rule 1: Numeric
    if re.match(r'^<NUM>$|\d+', word):
        return "NUM"
    
    # Rule 2: Punctuation
    if word in LEXICON["PUNC"]:
        return "PUNC"
    
    # Rule 3: Lexicon Lookup (Stemmed/Lemmatized)
    stem = urdu_stemmer(word)
    lemma = urdu_lemmatizer(word)
    
    for tag in LEXICON:
        if word in LEXICON[tag] or stem in LEXICON[tag] or lemma in LEXICON[tag]:
            return tag
            
    # Rule 4: Suffix heuristics
    if any(word.endswith(s) for s in ['نا', 'نے', 'نی', 'تا', 'تے', 'تی']):
        return "VERB"
    if any(word.endswith(s) for s in ['وں', 'یں', 'یاں']):
        return "NOUN"
        
    return "UNK"

def get_ner(tokens, idx):
    # Longest match for multi-word entities
    for entity_type in ["PER", "LOC", "ORG"]:
        for length in [3, 2, 1]:
            if idx + length <= len(tokens):
                phrase = " ".join(tokens[idx : idx + length])
                if phrase in GAZETTEER[entity_type]:
                    tags = [f"B-{entity_type}"]
                    for _ in range(length - 1):
                        tags.append(f"I-{entity_type}")
                    return tags
    return ["O"]

